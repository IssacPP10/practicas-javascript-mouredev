// Usar lowerCamelCase para nombrar variables es una buena práctica en JavaScript, esto significa que la primera palabra de la variable empieza con minúscula y las siguientes palabras empiezan con mayúscula.
// var

var helloWorld = "¡Hello World!";
console.log(helloWorld);

helloWorld = "!Hola de nuevo!";
console.log(helloWorld);

// La mejor manera de declarar variables en JavaScript es utilizando let y const, ya que var tiene problemas de scope y puede generar errores en el código.
// let
let helloWorld2 = "!Hola, JavaScript!";
console.log(helloWorld2);

helloWorld2 = "!Hola de nuevo, JavaScript!";
console.log(helloWorld2);

// const (al usar const debemos tener en cuenta que no podemos reasignar valores a la variable y que debemos inicializarla con un valor)
const helloWorld3 ="!Hola, JavaScript const!"
console.log(helloWorld3);

